
public class OperadoresAsignacion {
    public static void main(String[] args) {
        int a=5, b=2;
        int c=a;
        System.out.println("c=a= "+c);
        
        int d=a+5-b;
        System.out.println("d="+d);
        
        a+=1;//a=a+1
        System.out.println("a="+a);
        
        a+=4;//a=a+4
        System.out.println("a="+a);
        
        b-=1;//b=b-1
        System.out.println("b="+b);
        
    }
}
