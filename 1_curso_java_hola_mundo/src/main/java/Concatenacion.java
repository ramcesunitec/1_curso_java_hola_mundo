
public class Concatenacion {
    public static void main(String[] args) {
        
        var usuario="Ramces";
        var saludar="Hola";
        
        System.out.println(saludar+" "+usuario);
        
        var i=3;
        var j=5;
        
        System.out.println(i+j);
        System.out.println(saludar+i+j);
        System.out.println(i+j+saludar);
       
    }
}
